package fundamentos;
public class OperadoresDeComparacao {
    public static void main(String[] args) {
        int numero = 10;

        System.out.println(numero > 8);
        System.out.println(numero < 15);
        System.out.println(numero == 8);
        System.out.println(numero != 3);
        System.out.println(numero >= 7);
        System.out.println(numero <= 7);
    }
}
