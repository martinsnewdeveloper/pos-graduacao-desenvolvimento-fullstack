package fundamentos;
public class OperadoresAritmeticos {
    public static void main(String[] args) {
        int numero01 = 15;
        int numero02 = 9;

        System.out.println(numero01 + numero02);
        System.out.println(numero01 - numero02);
        System.out.println(numero01 * numero02);
        System.out.println(numero01 / numero02);
        System.out.println(numero01 % 2);
    }
}
